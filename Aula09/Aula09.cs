using System;

class Aula09
{
    static void Main()
    {
        int num=10;

        Console.WriteLine(num<<1);//Dobra o Valor (20)
        Console.WriteLine(num>>1);//Diminue pela metade (5)
        Console.WriteLine(num<<2);//Dobra o Valor 2 vezes (40)
        Console.WriteLine(num>>2);//Diminue pela metade 2 vezes(2.5 arrendado para (2))
    }
}